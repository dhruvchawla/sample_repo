package com.bluedart.trackdart.dto;

public class TrackGpsDto {
	private String fieldExecutive;
	private String trackTime;
	private String latitude;
	private String longitude;
	private String gpsSignal;
	private String speed;
	private String battery;
	/**
	 * @return the fieldExecutive
	 */
	public String getFieldExecutive() {
		return fieldExecutive;
	}
	/**
	 * @param fieldExecutive the fieldExecutive to set
	 */
	public void setFieldExecutive(String fieldExecutive) {
		this.fieldExecutive = fieldExecutive;
	}
	/**
	 * @return the trackTime
	 */
	public String getTrackTime() {
		return trackTime;
	}
	/**
	 * @param trackTime the trackTime to set
	 */
	public void setTrackTime(String trackTime) {
		this.trackTime = trackTime;
	}
	/**
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the gpsSignal
	 */
	public String getGpsSignal() {
		return gpsSignal;
	}
	/**
	 * @param gpsSignal the gpsSignal to set
	 */
	public void setGpsSignal(String gpsSignal) {
		this.gpsSignal = gpsSignal;
	}
	/**
	 * @return the speed
	 */
	public String getSpeed() {
		return speed;
	}
	/**
	 * @param speed the speed to set
	 */
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	/**
	 * @return the battery
	 */
	public String getBattery() {
		return battery;
	}
	/**
	 * @param battery the battery to set
	 */
	public void setBattery(String battery) {
		this.battery = battery;
	}
	
	
	
	
}
